    public modelEMPRESA validador() {
        modelEMPRESA oEmpresa = new modelEMPRESA();

        int ID = (txtid.getText().isEmpty() ? 0 : Integer.parseInt(txtid.getText()));

        oEmpresa.setID(ID);
        oEmpresa.setNit(txtnit.getText());
        oEmpresa.setRazonsocial(txtrazonsocial.getText());
        oEmpresa.setDireccion(txtdireccion.getText());
        oEmpresa.setCorreo(txtcorreo.getText());

        return oEmpresa;
    }

