    public void mostrarDatos() {

        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        try {
            //   desde el controlador hace el query y envia los datos a la interfaz
            ControllerEMPRESAS data = new ControllerEMPRESAS();
            ResultSet datos = data.queryGetData();

            while (datos.next()) {

                Object[] oUsuario = {datos.getString("ID"), datos.getString("nit"), datos.getString("razonsocial"), datos.getString("direccion"), datos.getString("correo")};

                modelo.addRow(oUsuario);
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }

